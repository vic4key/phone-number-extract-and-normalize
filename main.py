import phonenumbers

def normalize_phone_number(text: str) -> str:
	''' Normalize a phone number to standard international format.
	Eg. 0981234567
	'''
	result = ""
	try:
		tmp = phonenumbers.parse(text, "VN")
		result = f"0{tmp.national_number}"
	except Exception as e: pass
	return result

